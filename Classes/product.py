class Product:
    def __init__(self, id, name, sex, brand, season, price, discount_price, article, colors, sizes, image_path):
        self.id = id
        self.name = name
        self.sex = sex
        self.brand = brand
        self.season = season
        self.price = price
        self.discount_price = discount_price
        self.article = article
        self.colors = colors
        self.sizes = sizes
        self.image_path = image_path
